#include <vector>

using namespace std;

class Solution {
public:
    void reverseString(vector<char> &s) {
        int size = s.size();
        if (size < 1)
            return;
        for (int i = 0; i <= (size - 1) / 2; ++i) {
            swap(s[i], s[size - i - 1]);
        }
    }
};
